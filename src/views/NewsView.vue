<template>
    <div>
        <div v-for="user in users" v-bind:key="user">{{user.title}}</div>
    </div>
</template>

<script>
import {fetchNewsList} from '../api/index'

export default {
    data() {
        return {
            users: []
        }
    },
    created() {
        var vm = this;
        fetchNewsList()
            .then(response => {
                console.log(response);
                vm.users = response.data
            })
            .catch(error => 
                console.log(error));
    }
}
</script>

